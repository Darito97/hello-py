### Condicionales ###

# Condicional

my_condition = True

if my_condition:
    print("La condición es verdadera")


print("Fin de la condición")


# Condicional if con else

my_condition = False

if my_condition:
    print("La condición es verdadera")
else:
    print("La condición es falsa")
    
print("Fin de la condición if-else")